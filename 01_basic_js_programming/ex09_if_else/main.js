
/**
 * if
 * if/else
 * if/else if
 * if else if/else
 */

let x = 12.5;


// 1) if
console.log(`ex9.1`);
if(x > 10) {
    console.log(`x > 10`);
}


// 2) if/else
console.log(`ex9.2`);
if(x > 15) {
    console.log(`x > 15`);
}
else {
    console.log(`x <= 15`);
}


// 3) if/if else
console.log(`ex9.3`);
if(x > 15) {
    console.log(`x > 15`);
}
else if (x > 10) {
    console.log(`x > 10`);
}


// 4) if/if else/else
console.log(`ex9.4`);
if(x > 15) {
    console.log(`x > 15`);
}
else if (x > 10) {
    console.log(`x > 14`);
}
else {
    console.log(`x <= 14`)
}
