/**
 * Variable declaration.
 */
let b = true;                       // Boolean variable, can be `true` or `false`
let i = 10;                         // Integer variable
let f = 3.15;                       // Floating-point (real number) variable
let s = "JavaScript";               // String variable
let o = {name: "abc", value: 1.22}  // Object variable

/**
 * Print messages to the console/terminal window.
 */
console.log('Welcome to JavaScript Programming');
console.log("b = " + b);
console.log("i = " + i);
console.log("f = " + f);
console.log("s = " + s);
console.log("o=" + o)
console.log(o)

/**
 * Print the messages using the `${}`
 */
console.log(`b = ${b}`);
console.log(`i = ${b}`);
console.log(`f = ${b}`);
console.log(`s = ${b}`);
console.log(`o = ${o}`);


