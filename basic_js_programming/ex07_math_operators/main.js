
/**
 * Mathematic Operators
 *  +, -, *, /, **, %, ++, --, +=, -=
 */

let x = 2.5;
let y = 2.0;

// 1) Addition operator
console.log(`x + y = ${x + y}`);

// 2) Subtraction operator
console.log(`x - y = ${x - y}`);

// 3) Multiplication operator
console.log(`x * y = ${x * y}`);

// 4) Division operator
console.log(`x / y = ${x / y}`);

// 5) Exponentiation operator
console.log(`x ** y = ${x ** y}`);

// 6) Modulus (Division Remainder)
console.log(`10 % 2 = ${10 % 2}`);
console.log(`10 % 4 = ${10 % 4}`);
console.log(`10 % 6 = ${10 % 6}`);

// 7) Increment operators (++ and +=)
let a = 2;
a++;
console.log(`a++ = ${a}`);
a += 3;
console.log(`a+=3 = ${a}`);

// Decrement operators (-- and -=)
let b = 8;
b--;
console.log(`b++ = ${b}`);
b -= 5;
console.log(`b-=5 = ${b}`);
