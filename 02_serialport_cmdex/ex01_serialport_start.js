/**
 * 1) Initialize the project
 * Run the command `npm init -y`
 */

/**
 * 2) Modify the `package.json` to support `module`
 * Add the `"type": "module",` in to the `package.json`
 */

/**
 * 3) Install the `serialport` package
 * Run the command `npm install serialport`
 */

/**
 * 4) Connect the MCU board to the computer using USB cable
 */

/**
 * Serial port checking
 * Run the command `npx @serialport/list`
 * If it asks to install additional package, choose `y`
 * 
 * The serial port name(s) will be shown in the terminal.
 * Check which one is the port connected to the MCU.
 * 
 * In Windows:  `COMxx`
 * In Mac:      `/dev/tty.xxx`
 */